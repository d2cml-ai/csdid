GAPS_REF = os.path.join(REF_DIR, "sim", "ref_gaps.csv")
GAPS_DATA = os.path.join(REF_DIR, "sim", "mpdta_extra.csv")

# scenario -> (constructor kwargs, fit kwargs)
GAP_SCENARIOS = {
    "rc":            (dict(panel=False), dict(base_period="varying")),
    "universal":     (dict(), dict(base_period="universal")),
    "anticipation1": (dict(anticipation=1), dict(base_period="varying")),
    "weighted":      (dict(weights_name="wt"), dict(base_period="varying")),
    "clustered":     (dict(clustervar="clust"), dict(base_period="varying")),
}


@pytest.mark.skipif(
    not (os.path.isfile(GAPS_REF) and os.path.isfile(GAPS_DATA)),
    reason="gap-scenario R reference not generated",
)
@pytest.mark.parametrize("scn", list(GAP_SCENARIOS))
def test_gap_scenarios_match_r(scn):
    """RC / universal / anticipation / weighted / clustered-SE match R `did`."""
    ref = pd.read_csv(GAPS_REF)
    ref = ref[ref["scenario"] == scn]
    rkey = {(int(r.group), int(r.t)): (float(r.att), float(r.se)) for _, r in ref.iterrows()}
    data = pd.read_csv(GAPS_DATA)
    ikw, fkw = GAP_SCENARIOS[scn]
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        res = ATTgt(yname="lemp", tname="year", idname="countyreal", gname="first.treat",
                    data=data, control_group="nevertreated", **ikw).fit(
                        est_method="reg", bstrap=False, **fkw)
    g = np.asarray(res.MP["group"], dtype=float)
    t = np.asarray(res.MP["t"], dtype=float)
    att = np.asarray(res.results["att"], dtype=float)
    se = np.asarray(res.results["se"], dtype=float)
    n_checked = 0
    for gi, ti, a, s in zip(g, t, att, se):
        key = (int(gi), int(ti))
        if key not in rkey:
            continue
        ra, rs = rkey[key]
        np.testing.assert_allclose(a, ra, atol=1e-6, err_msg=f"{scn} ATT g={key[0]} t={key[1]}")
        if np.isfinite(s) and np.isfinite(rs):
            np.testing.assert_allclose(s, rs, rtol=0.03, atol=5e-4,
                err_msg=f"{scn} SE g={key[0]} t={key[1]}")
        n_checked += 1
    assert n_checked == len(rkey)



